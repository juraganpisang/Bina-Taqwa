                                        <h3>PELAKSANAAN BIMBINGAN MANASIK HAJI</h3>
                                        <hr size="3px">
                                        <p>
                                            KBIH ILTIZAM melaksanakan bimbingan manasik haji 1440 H setiap hari Ahad, dimulai tanggal dimulai <b>17 Februari s.d 21 April 2019</b>
                                             (10 kali pertemuan), bertempat di Auditorium Politeknik Negeri Malang. Mulai jam 07.30 s.d. 11.45 WIB. Dengan materi dua sesi setiap pertemuan. Dalam pelaksanaan manasik dilakukan dua kali praktek ibadah langsung, yaitu pada pertemuan ke-6 dan ke-10.
                                        </p>
                                        <p>
                                        Untuk kesempurnaan manasik, dilaksanakan pemantapan dan pembinaan pada tarawihan keliling di rumah calon jamaah haji setiap hari Sabtu dan Ahad malam 
                                        (8 kali pertemuan) ditambah <i>briefing</i> akhir sebelum keberangkatan yang dilaksanakan sesudah Idul Fitri, berupa Halal bihalal dan jalan sehat bersama setelah mendapatkan kepastian kloter. Total jumlah pertemuan bimbingan manasik yang dilaksanakan adalah 20 kali pertemuan.
                                        </p>
